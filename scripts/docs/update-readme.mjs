import { execSync } from "node:child_process";
import fs from "node:fs";

const README_PATH = "README.md";
const START_MARKER = "<!-- README:AUTO:START -->";
const END_MARKER = "<!-- README:AUTO:END -->";

function safeExec(command) {
  try {
    return execSync(command, { encoding: "utf8", stdio: ["ignore", "pipe", "ignore"] }).trim();
  } catch {
    return "";
  }
}

function readPackageVersion() {
  const pkg = JSON.parse(fs.readFileSync("package.json", "utf8"));
  return String(pkg.version || "0.0.0").replace(/^v/i, "").trim() || "0.0.0";
}

function getLatestTag() {
  return safeExec("git tag --list 'v[0-9]*.[0-9]*.[0-9]*' --sort=-v:refname | head -n 1") || "none";
}

function getBranch() {
  return safeExec("git rev-parse --abbrev-ref HEAD") || "unknown";
}

function getHeadCommit() {
  return safeExec("git rev-parse --short HEAD") || "none";
}

function buildSection() {
  const version = readPackageVersion();
  const latestTag = getLatestTag();
  const branch = getBranch();
  const headCommit = getHeadCommit();
  const generatedAt = new Date().toISOString();

  return [
    START_MARKER,
    "## Repository Snapshot",
    "",
    "This section is generated automatically on every commit.",
    "",
    `- Package version: \`${version}\``,
    `- Latest release tag: \`${latestTag}\``,
    `- Branch: \`${branch}\``,
    `- Current HEAD before commit: \`${headCommit}\``,
    `- Updated at (UTC): \`${generatedAt}\``,
    "",
    "Generated by `npm run readme:update` via `.githooks/pre-commit`.",
    END_MARKER,
  ].join("\n");
}

const readme = fs.readFileSync(README_PATH, "utf8");
const section = buildSection();

if (readme.includes(START_MARKER) && readme.includes(END_MARKER)) {
  const pattern = new RegExp(`${START_MARKER}[\\s\\S]*?${END_MARKER}`, "m");
  fs.writeFileSync(README_PATH, readme.replace(pattern, section));
} else {
  const next = `${readme.trimEnd()}\n\n${section}\n`;
  fs.writeFileSync(README_PATH, next);
}
